package com.tulettPrebon.price;

public class Configuration {

	public int displayFormat;
	
	public Integer scale;
	
	public Integer dpl;
	
	public Integer fpl;

	public int getDisplayFormat() {
		return displayFormat;
	}

	public void setDisplayFormat(int displayFormat) {
		this.displayFormat = displayFormat;
	}

	public Integer getScale() {
		return scale;
	}

	public void setScale(Integer scale) {
		this.scale = scale;
	}

	public Integer getDpl() {
		return dpl;
	}

	public void setDpl(Integer dpl) {
		this.dpl = dpl;
	}

	public Integer getFpl() {
		return fpl;
	}

	public void setFpl(Integer fpl) {
		this.fpl = fpl;
	}
	
	
}
